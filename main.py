    await bot.send_document(user_id, gif_file, caption=description)

    # –û–±–Ω–æ–≤–ª—è–µ–º –≤—Ä–µ–º—è –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –∫–æ–º–∞–Ω–¥—ã
    user_last_daily_forecast[user_id] = datetime.datetime.now()

    # –°–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É —Å –∫–Ω–æ–ø–∫–∞–º–∏ –º–µ–Ω—é
    keyboard = ReplyKeyboardMarkup(resize_keyboard=True, one_time_keyboard=True, keyboard=[
        [KeyboardButton(text="–ü—Ä–æ–≥–Ω–æ–∑ –Ω–∞ –¥–µ–Ω—å üôè")],
        [KeyboardButton(text="‚≠ê –ú–æ—ó –ø–æ—Å–ª—É–≥–∏")],
        [KeyboardButton(text="–ú—ñ–π —Ä–∞—Ö—É–Ω–æ–∫ üí∞")],
        [KeyboardButton(text="‚ùì‚ùì‚ùì –ó–∞–¥–∞—Ç–∏ –ø–∏—Ç–∞–Ω–Ω—è")]
    ])

    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ —Å –º–µ–Ω—é
    await bot.send_message(user_id, "–í–∏–±–µ—Ä—ñ—Ç—å –ø—É–Ω–∫—Ç –º–µ–Ω—éüëá:", reply_markup=keyboard)

